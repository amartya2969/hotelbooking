        DefaultTableModel doubleModel = (DefaultTableModel) doubleRoomTable.getModel();
        DefaultTableModel familyModel = (DefaultTableModel) familyRoomTable.getModel();

        int singleTotal = 0, doubleTotal = 0, familyTotal = 0;
        double bill = 0;
